void init_printer() {
  vga_clear();
  for (int x = 4; x < w - 4; x++) {
    for (int y = 4; y < h - 4; ++y) {
      int r = ((w/2-x | h/2-y) + ((w/2-x)*(w/2-x) + (h/2-y)*(h/2-y) >> (x & 11))) % 0x100 & 0xff;
      int g = ((w/2-x ^ h/2-y) + ((w/2-x)*(w/2-x) + (h/2-y)*(h/2-y) >> (y & 9))) % 0x100 & 0xff;
      int b = ((w/2-x & h/2-y) + ((w/2-x)*(w/2-x) + (h/2-y)*(h/2-y) >> (x - y & 11))) % 0x100 & 0xff;
      getptr(0, 0, y * w + x) = r << 16 | g << 8 | b;
    }
  }
  curx = 0;
  cury = 0;
}

